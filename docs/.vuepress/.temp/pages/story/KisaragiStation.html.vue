<h2 id="第三部分-列车二车厢" tabindex="-1"><a class="header-anchor" href="#第三部分-列车二车厢" aria-hidden="true">#</a> 第三部分：列车二车厢</h2>
<p>列车二车厢看上去像是一间教室，比列车本身应有的大小要大很多。但是窗外仍是之前的景象。教室内仅在靠窗的角落有两个桌椅，其中一个的桌面上端正地摆着一束白百合，另外一个座位是空的。教室的后面有学生用储物柜和用于放置扫除用具的柜子。从教室的后门离开就会到达列车的三车厢</p>
<ul>
<li>摆着花的桌洞里有一个玩偶，少女会认为这是很重要的东西，而知道自己是莲实的情况下会告诉玩家这是如月送给她的生日礼物。
<ul>
<li>这个玩偶看起来是按照莲实的样子制作的，并且会与少女身体的破碎同步，玩偶也会有部分的缺失</li>
<li>对玩偶调查的检定成功后，会发现玩偶内部有一张存储卡，当玩家把存储卡靠近手机时，手机就会弹出收到电话的提示，点开后会听到下面的语音直接外放
<blockquote>
<p>马上到站蛛懊 ∪ 繧峨 ↑ 縺从背后蛻髮｢縺励 ↑ 縺摔了下去縺翫 ° 縺电车停下来了</p>
<ul>
<li>乱码部分为日文 JIS 编码至 UTF8 编码</li>
<li>原文：（列车）马上到站，（我）停不下来，从背后（抱住她），不会分开，（一起）摔了下去，好奇怪啊，电车停下来了</li>
</ul>
</blockquote>
</li>
</ul>
</li>
<li><strong>黑板</strong>上写着“祝贺毕业”，但是被擦掉了只剩下痕迹。</li>
<li><strong>储物柜</strong>的柜子都上着锁，仅有座位后面的柜子半掩着。里面是一个日记本</li>
<li>玩家会感觉花上有异常的魔力波动，需要玩家主动进行共鸣。检定成功后，会看到回忆中的二车厢。</li>
<li>回忆·二车厢中光线变得灰暗。两个桌椅只剩下了空的那一个。而带花的那个，椅子则被倒扣在桌子上。推到了角落
