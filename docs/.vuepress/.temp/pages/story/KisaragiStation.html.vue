<li>玩家在浓雾中探索时，会发现月台周围的地面上有列车的轨道，如果玩家沿着轨道前进一段距离会发现轨道沿着月台绕了一圈后又回到了月台边（灵感检定）。</li>
<li>玩家向着更远处探索时，会发现自己不知不觉回到了月台。</li>
<li>在沿着轨道前进时，玩家会听到列车即将到站的报站声，之后玩家进行幸运检定，如果检定失败则会被突然出现的列车撞飞，损失 1D6 的 HP，并回到月台边。</li>
</ul>
</li>
<li>当玩家探索完成后，主持人便可以让列车从浓雾中驶入，停靠在站台
<ul>
<li>玩家们会注意到地面上什么也没有的地方出现了铁轨，列车随后驶来</li>
<li>如果玩家长时间不上车，车站的灯光就会闪烁，此时 SAN 值低于一半的玩家都会在闪烁时看到一个少女被列车撞死的景象，并损失 1D4/1D8 的 SAN 值。随后列车开始鸣笛，准备发车。具体的时长可以根据玩家的反映来调整</li>
<li>如果玩家们执意不上车，则可以直接跳到最后一个场景直接面对崩坏化身。</li>
</ul>
</li>
</ul>
</li>
<li><strong>轨道</strong> 月台下的轨道乍一看没有什么异常，但如果<strong>侦查</strong>检定成功，会发现轨道上有着淡淡的、快要消失的血迹。</li>
</ul>
<h2 id="第二部分-列车一车厢" tabindex="-1"><a class="header-anchor" href="#第二部分-列车一车厢" aria-hidden="true">#</a> 第二部分：列车一车厢</h2>
<p>列车车厢是普通的列车布置，但是两侧连通的车门是锁死的。车窗的外面仍在下着“雨”，地面上是荒野，远处仍是浓雾。</p>
<ul>
<li>无论在哪个车厢跳窗，都会到迷雾中。当玩家回到月台后列车就会从浓雾中出现并停在月台旁。</li>
</ul>
<p>在座位下面有一个 <strong>行李箱</strong></p>
<p>一侧车门的外面有一个 <strong>少女</strong> （也可以是和人设图有一定差别的幼女），是对于莲实的记忆在崩坏中的魔力的具现化，也是会影响结局的重要因素。在玩家进入车厢后就在车门外偷偷观察（当然会避开玩家探索的目光，除非被反侦察到，由于车厢的隔音，也不会被 <strong>聆听</strong> 所察觉到），当玩家探索进入一阶段后就开始敲玻璃吸引玩家的注意力。而在她的指引下，玩家可以发现车门上的紧急按钮，并打开通往二车厢的门。</p>
<ul>
<li><strong>行李箱</strong> 行李箱的密码是 915，当然也可以通过暴力打开。当玩家打开行李箱时，整个车厢突然会堆满相片，而行李箱中是一个手机。
<ul>
<li><strong>相片</strong> 相片中全部是如月和莲实二人的合照，但是玩家并不知道二人的长相，可以按照二人的设定图来描述。
<ul>
<li>所有照片中，莲实的位置都变得模糊不清，当玩家仔细观察时，会进行一个灵感的判定。一旦判定过在查看其他照片时就不会再触发判定。
<ul>
<li>判定成功时，玩家会发现莲实的位置变得支离破碎，身体以不正常的方式扭曲着。但是大脑却认为这是一个正常且完整的人，虽然无法辨认相貌但是可以识别出照片中的动作。玩家需要进行一个 0/1D3 的 SC。</li>
<li>将<strong>少女</strong>带至一车厢后，判定成功的玩家能够辨认出照片中莲实的大概相貌</li>
<li>判定失败时，玩家只会觉得照片上模模糊糊，无法辨认</li>
</ul>
</li>
<li>照片上的内容都是二人的日常互动，例如一起吃可丽饼、互相吃对方的冰淇淋、睡衣派对、拍大头贴……总之是各种贴贴的美少女互动行为，只要玩家一直看可以一直编</li>
</ul>
</li>
<li><strong>手机</strong> 手机的所有界面都一直在抖动，所有文字都模糊不清，屏幕时而变色时而出现条纹。玩家会明显感觉继续看下去会有不好的事情发生，但是能察觉到手机上有明显的魔力波动。当玩家选择继续观看的时候，所有看着屏幕的玩家对于一车厢的观察会被回忆·一车厢的场景强制替换，并因为辨认扭曲怪异的界面而损失 1/1D3 的 SAN 值。
<ul>
<li><strong>回忆·一车厢</strong> 回忆中的车厢看起来像是一个商业街的小巷，座椅被商业街的座椅替换，而车厢则变成了墙壁。座椅上坐着两位少女正在贴贴的幻象，然而代表莲实的少女扭曲程度比之照片上有过之而无不及。二车厢另一边的车门，此时变成了一个甜品店。手机上的文字也可以辨认了。
<ul>
<li><strong>少女</strong> 当少女看到贴贴场景时，会突然泪流满面，并会下意识呼唤“如月”之类的字眼，而且仍然意识不到原因。</li>
<li><strong>手机</strong> 手机打开后会显示一个甜品店的优惠券</li>
<li><strong>甜品店</strong> 甜品店的店员外貌并不重要，可以随意描述。店员会正常与玩家交流，但是内容都是建立在现实中的甜品店基础上的。不过根据玩家问的问题还是会有一些特殊的回答作为线索：
<ul>
<li>与店员对话，向她展示手机上的优惠券，她会送给玩家一个<strong>可丽饼</strong>。将可丽饼给少女吃可以让少女回忆起一部分自己，使少女的状态恢复一次。少女会觉得是令人怀念的味道，曾经和谁一起吃过。毕竟是魔力的聚合体，如果由玩家吃掉的话……魔法少女会肚子痛吗</li>
<li>“她们两个可是这里的常客”或者类似的回答，作为提示让玩家帮助莲实回忆自己。</li>
</ul>
</li>
<li>当玩家想要离开甜品点时，回忆就会消失回到一车厢的景象，手机会黑屏</li>
</ul>
</li>
</ul>
</li>
</ul>
</li>
<li><strong>少女</strong> 少女不记得自己是谁，也不知道这里是哪里。但是会哀求玩家，让玩家帮她想起自己是谁，并声称如果她想不起自己的身份，将会有不好的事情发生。在玩家打开车门后，会请求跟随玩家一起行动。当玩家获得关键线索时，少女会下意识有所反应，具体根据主持人的想法来自由发挥。
<ul>
<li>要点：知晓少女姓名是帮助回忆的充分不必要条件。但获知与如月的友谊则是充要条件。</li>
<li>玩家每经过一段时间的调查（每次大概 20min？具体由主持人把握），都会使得少女身体的一部分变得视觉上破碎。影响少女的行动并扣除 3HP 同时会让少女的精神变得更加混乱。
<ul>
<li>这是一个渐变的过程，玩家可以看到破碎的位置在逐渐扩展。只是方便记录使用次数来计算。虽然设想上是像素故障那种破碎，不过具体如何描述可以按照主持人风格。</li>
<li>每当一次转变后，少女都会如同梦游式地说出一些关于她身份的信息（比如几个音节、和如月贴贴的日常等等），但少女不会意识到这些话语。</li>
<li>而四次后，少女就会失去理智，对玩家发起攻击，失去理智后破碎的身体仍然可以行动。当玩家告诉少女她自己是谁时，就不再会触发新的破碎，同时精神回复。但是已经破碎的肢体不会复原。</li>
<li>当少女意识到自己的身份，就会请求玩家把她带到如月身旁</li>
</ul>
</li>
<li>少女的属性可以现车一个属性卡，也可以使用下方的随便车的卡，少女按照普通人来判定所以力量体质等会有惩罚骰。
