<h2 id="第五部分-回到月台" tabindex="-1"><a class="header-anchor" href="#第五部分-回到月台" aria-hidden="true">#</a> 第五部分：回到月台</h2>
<p>随着游戏进行，雨点会变得越来越大。最终玩家会听到外面有巨响，从车窗向外看去会发现巨大的玩偶、可丽饼（或者其他什么东西）像小山一样插进地面里。而她们的头顶也出现了一片阴影，并且逐渐扩大，显然有什么东西正要砸下来。</p>
<ul>
<li>这时如果玩家选择从车门或是其他地方逃走，则需要进行闪避检定，成功的话就不会受到伤害，而失败的话就会受到 1D6 的伤害，少女也需要进行检定，除非她正在被谁保护着。</li>
<li>如果玩家选择硬抗，则按照武器的抵抗来计算。伤害为 1D9。</li>
</ul>
<p>玩家用任何方法离开或者硬抗下来后，都会发现自己回到了月台上。</p>
<p>当玩家们回到月台时，会发现月台的空间发生了变化。地面是荒野，路灯、长椅、玩偶各种物品在地面上随机分布一直到浓雾中，但是并不到影响行动的程度也无法当作掩体。而在她们面前有一片空白区域，中心则是崩坏化身。</p>
