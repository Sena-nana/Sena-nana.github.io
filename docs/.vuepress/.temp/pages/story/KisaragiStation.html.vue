<h3 id="少女的物语" tabindex="-1"><a class="header-anchor" href="#少女的物语" aria-hidden="true">#</a> 少女的物语</h3>
<p>初中时，如月也曾是普通的女孩子。她和其他人一样，“融入”了班上女孩子的小圈子。但是，小圈子中出现了一些矛盾，为了转移这些矛盾，领头人将过错抛给了当时就有些内向的她。一瞬间，她曾经相信的朋友就开始视她为异己。她开始被欺凌，虽然并不是很过分，并且立即被家长和老师发现。于是她立即转学到了老家的学校。在新的学校，她害怕融入任何的圈子。她变得越来越内向，却又无比羡慕着有着朋友的人。于是，在成为高中生的时候，<strong>想要有人可以一起上下学，想要有人可以分享一切，想要一个真正的朋友</strong>，这么想着，QB 回应了她。</p>
<p>她遇到了莲实，一个并不热情，并不可爱，和她一样没有小圈子，却时时刻刻为她着想的人。如月像是一个粘人的妹妹一样，上学时跟在莲实身后，放学后跟在莲实身后，甚至假期，也会时不时一起外出。在莲实的带领下，她第一次和别人一起吃过可丽饼，一起夹过玩偶，一起坐过摩天轮。她们自己也没有注意到，她们在一起的时间，已经远远超过了独处的时候。</p>
<p>而在高中毕业之后，莲实考上了国外的名校，如月却名落孙山，不过，只要莲实考上了，她也没有什么在意的，本来，是这么想的。直到莲实离开的那一天，她才发现，自己已经无法离开莲实。一想到莲实之后无法在自己身边，她能感觉到的只有恐惧和迷茫。自己又将孤身一人，自己又会回到初中时被排挤的状态。她不敢去想，开始疯狂地追赶，一路上不知撞到了几个人，也不知鞋子丢在了哪里。视野已经模糊，在出现莲实的身影的时候，她什么也没有想就飞扑了上去。</p>
<p>此时，莲实正站在月台的边缘等候着列车。而列车的车头已经进站，正在减速的过程中。毫无预料的背后飞扑，让莲实脚下一滑，随后便是一声巨响。当如月清醒过来时，映入她眼帘的是莲实浑身带血的身影。当她意识到自己的行为时，灵魂宝石已变得漆黑。随后，崩坏发生了。</p>
