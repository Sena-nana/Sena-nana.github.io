<li>如果玩家想要徒手擦掉印记，则液体会粘在玩家的手上。并在玩家回到灯光下时注意到这件事。此时玩家会立即察觉到一股铁锈味并需要进行一个灵感检定
<ul>
<li>当灵感检定失败时，玩家仅会觉得这是令人作呕的不明液体，并会因为自己想象力造成的不安损失 0/1D3 的 SAN 值</li>
